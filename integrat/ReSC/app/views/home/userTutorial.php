           <h3>2. Trimiterea mesajelor</h3>
           <p>
               Un vizitator(utilizator fara cont) poate sa trimita un mesaj administratorului de pe pagina in cazul in care are o 
               anumita nelamurire.
               Pentru a trimite un mesaj administratorului trebuie sa apasam pe butonul de chat aflat in partea din dreapta jos 
               in pagina principala. 
           </p>
           <img src="../../img/userTutorial/chat_box.PNG" alt="Header" class="documentation__images">
           <p>
               Dupa apasarea butonui de chat o sa se deschida urmatoarea fereastra
           </p>
           <img src="../../img/userTutorial/fig3_chat.PNG" alt="Header" class="documentation__images">
             <p>
                 Clientul este nevoit sa completeze cu un username pentru a putea utiliza chatul.
                 Daca client este conectat cu un cont poate utiliza chatul fara a specifica un username  . 
                Utilizatorul poate trimite acum mesaje catre un admin sau poate trimite mesaje intr-un chat in care se afla
                toti utilizatorii.
                Pentru a trimite un mesaj trebuie sa scrie ceva in "Scrie ceva.." si sa apese butonul "Send" sau direct tasta enter.
                </p>

             <img src="../../img/userTutorial/fig4_chat.PNG" alt="Header" class="documentation__images">
             <img src="../../img/userTutorial/fig7_chat.PNG" alt="Header" class="documentation__images">
          
             <p>
             Utilizatorul poate schimba tema chatului dupa bunul plac.
             Butonul din dreapta jos ne permite sa inchidem conversatia curenta si ne arata lista conversatiilor pe care le putem accesa
             .In partea de jos putem alege tema si marimea pe care sa o aiba mesajul.
             Tema este lightmode sau darkmode si pentru a realiza schimbarea trebuie sa apasam si pe
             butonul "Change appearence".
         </p>
         <img src="../../img/userTutorial/fig5_chat.PNG" alt="Header" class="documentation__images">
         <img src="../../img/userTutorial/fig6_chat.PNG" alt="Header" class="documentation__images">

         <h2>3.Pagina adminului</h2>
            <p>Pentru a accesa pagina adminului trebuie sa ne conectam cu un cont ce are rolul de Admin.
                Odata conectati cu un cont de admin apare in meniul  din pagina principala  butonul "Admin Panel".
            </p>
            
         <img src="../../img/userTutorial/fig1_admin.PNG" alt="Header" class="documentation__images">

         <p>
             Apasand pe buton o sa fim redirectionati catre pagina adminului.
         </p>
         <img src="../../img/userTutorial/fig2_admin.PNG" alt="Header" class="documentation__images">

         <p>
             Adminul are acces la toate conversatiile si pentru a deschide una dintre ele trebuie sa  apese pe butonul "Chat" al conversatiei.
         </p>
         <img src="../../img/userTutorial/fig3_admin.PNG" alt="Header" class="documentation__images">

         <p>
            <p>
                Administratorul poate trimite un mesaj completand in panoul din mijloc in "Scrie ceva.." si prin apasarea tastei enter sau
                pe butonul de send.
                Daca doreste sa deschida o alta conversatie trebuie sa o inchida pe cea curenta.

            </p>
            <p>
                Tema  se poate modifica schimband in al treilea panou din DarkMode in LightMode(sau invers) si apasarea butonului
                "Apply".
            </p>
            <img src="../../img/userTutorial/fig4_admin.PNG" alt="Header" class="documentation__images">

         
